       public  int[,] LoadMap(int index,string path) {

           int[,] map = new int[15, 42];
            StreamReader sr = new StreamReader(path+index+".txt");
            String word;
            string val = "";
            int k = 0;

            for (int i = 0; i < 15; i++)
            {
                word = sr.ReadLine();
                k = 0;
               

                for (int j = 0; j < 42; j++)             
                {
                    
                    val = "";

                    while (word[k] != ' ')
                    {
                        val += word[k];
                        k++;

                    }
                    map[i, j] = int.Parse(val);
                    k++;

                }

            }


            sr.Close();
           


        return map;
        
        
        }

        

     
       

